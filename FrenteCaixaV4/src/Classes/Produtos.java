package Classes;

/**
 *
 * @author WEB02
 */
public class Produtos {

    private int id;
    private float preco;
    private String nome;

    public Produtos(int id, float preco, String nome) {
        this.id = id;
        this.preco = preco;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getPreco() {
        return preco;
    }

    public void setPreco(float preco) {
        this.preco = preco;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
